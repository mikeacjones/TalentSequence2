-- Decode a base-36 character to a grid index (0-based)
local function DecodeGridChar(ch)
    local byte = string.byte(ch)
    if byte >= 48 and byte <= 57 then -- '0'-'9'
        return byte - 48
    elseif byte >= 97 and byte <= 122 then -- 'a'-'z'
        return byte - 87
    end
    return nil
end

function ts.WarcraftTavernTalents.IsURL(input)
    return strfind(input, "warcrafttavern") ~= nil
end

local function ParseURL(input)
    local url = input:match("^%s*(.-)%s*$")
    if url == "" then return nil end

    local flavor, classSlug = url:match("warcrafttavern%.com/([^/]+)/tools/talent%-calculator/([^/#?]+)")
    if not flavor or not classSlug then return nil end

    classSlug = strlower(classSlug)
    flavor = strlower(flavor)

    local normalizedFlavor = FLAVOR_MAP[flavor]
    if not normalizedFlavor then return nil end

    local classToken = CLASS_TOKENS[classSlug]
    if not classToken then return nil end

    local encoded = url:match("#%??t=([^&]+)")
    if not encoded or encoded == "" then return nil end

    return normalizedFlavor, classToken, encoded
end

function ts.WarcraftTavernTalents.GetTalents(input)
    local flavor, classToken, encoded = ParseURL(input)
    if not flavor then
        return nil, nil, "INVALID_URL"
    end

    local startingLevel = STARTING_LEVELS[flavor]
    if not startingLevel then
        return nil, classToken, "IMPORT_FAILED"
    end

    local classGridMap = GRID_TO_INDEX[flavor] and GRID_TO_INDEX[flavor][classToken]
    if not classGridMap then
        return nil, classToken, "IMPORT_FAILED"
    end

    local talents = {}
    local currentTab = -1
    local pointsSpent = 0
    local rankCounter = {}

    for i = 1, strlen(encoded) do
        local ch = strsub(encoded, i, i)

        if ch == "A" then
            currentTab = 0
        elseif ch == "B" then
            currentTab = 1
        elseif ch == "C" then
            currentTab = 2
        else
            if currentTab < 0 then
                return nil, classToken, "IMPORT_FAILED"
            end

            local gridIdx = DecodeGridChar(ch)
            if not gridIdx then
                return nil, classToken, "IMPORT_FAILED"
            end

            local treeGridMap = classGridMap[currentTab]
            if not treeGridMap then
                return nil, classToken, "IMPORT_FAILED"
            end

            local talentIndex = treeGridMap[gridIdx]
            if not talentIndex then
                return nil, classToken, "MAPPING_FAILED"
            end

            pointsSpent = pointsSpent + 1

            local counterKey = tostring(currentTab) .. ":" .. tostring(talentIndex)
            rankCounter[counterKey] = (rankCounter[counterKey] or 0) + 1
            local rank = rankCounter[counterKey]

            local spellId = ts.TalentResolver and
                ts.TalentResolver.GetSpellId(classToken, currentTab + 1, talentIndex, rank)

            tinsert(talents, {
                tab = currentTab + 1,
                index = talentIndex,
                rank = rank,
                level = startingLevel + pointsSpent,
                spellId = spellId,
            })
        end
    end

    if #talents == 0 then
        return nil, classToken, "IMPORT_FAILED"
    end

    return talents, classToken
end
